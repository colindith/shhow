	static enum PRASER_TYPE {STK_DAY, TRUST_BUY}	//This function remove all column name!!
	static ArrayList<String> Parser(String htmlSrc, PRASER_TYPE type){
		ArrayList<String> tokenArray = new ArrayList<String>();
		String buff="";
		int mode=0;
		//mode 0: not , 1:not recording, 2: find a "<td", 3: find >, starting recording
		for(int i=3; i<htmlSrc.length(); i++) {
		//	System.out.print("-i="+i+"-");
		//	System.out.print(s.charAt(i));
			char c=htmlSrc.charAt(i);
			if(mode==0 &&
				htmlSrc.charAt(i-1)=='間' && 
				c=='僅') {
				mode=1;
				System.out.println("find 間僅");
			}
			else if(mode==0 &&
					htmlSrc.charAt(i-1)=='交' && 
					c=='筆') {
					mode=1;
					System.out.println("find 交筆");
				}
			else if(mode==1 &&
				htmlSrc.charAt(i-2)=='<' && 
				htmlSrc.charAt(i-1)=='t' && 
				c=='d') {
				mode=2;		//pass <td, set mode 
				System.out.println("find <td");
			}
			else if(mode==2 && htmlSrc.charAt(i)=='>') {
				System.out.println("Start recording");
				mode=3;		//find >, end of td, set mode 3
				//if(s.charAt(i+1)=='<') mode=1;	//<td> contents nothing (blank be included)
				
			}
			else if(mode==3 && c=='<') {
				if(buff.equals("合計買賣超")) {
					System.out.print("合計買賣超");
					return tokenArray;
				}
				tokenArray.add(buff);
				System.out.print("+buff="+buff+"+");
				buff="";
				mode=1;
			}
			else if(mode==3) {
				if(c!=0x20 && c!=',') {		//remove " " and "," in cell
					buff+=c;
				//	System.out.println("-"+s.charAt(i)+"-");
				}
			}
			//else if(mode==1 || mode==2 || mode==3) {mode=1;}
		}
		if(type==PRASER_TYPE.TRUST_BUY) {
			for(int i=0;i<11;i++) tokenArray.remove(0);
		}
		else if(type==PRASER_TYPE.STK_DAY) {
			for(int i=0;i<9;i++) tokenArray.remove(0);
		}
		System.out.println(tokenArray);
		return tokenArray;
		
	}
	static void writeStkFile(ArrayList<String> tokenArray, String fileName) {//未測試
		int i=0;
		byte[] BOM_UTF8 = { (byte) 0xEF, (byte) 0xBB, (byte) 0xBF };
		try (FileOutputStream writerByte = new FileOutputStream(fileName,false)){
			writerByte.write(BOM_UTF8);

			for(String s: tokenArray) {
				if(!s.isEmpty()) {
					writerByte.write((s+",").getBytes("utf8"));
					i++;
					if(i%9==0) {
						writerByte.write("\n".getBytes("utf8"));
					}
				}
			}
		} catch (Exception e3) {
			System.out.println("write byte error: "+e3);
		}
		System.out.println("done!");
	}
